#include <unistd.h>

void	ft_print_comb2(void);

void	ft_print_comb2(void)
{
	char	a[2];
	char	b[2];

	a[0] = '0';
	while( a[0] <= '98')
	{
		b = a + 1;
		while (b <= '99')
		{
			write(1, &a, 2);
			write(1, " ", 1);
			write(1, &b, 2);
			if (!(a == '98' && b == '99'))
				write(1, ", ", 2);
				b++;
		}
		a++;
	}
}
